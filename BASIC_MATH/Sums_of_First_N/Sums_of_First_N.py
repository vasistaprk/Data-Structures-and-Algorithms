"""
Sum Of First N natural Numbers:
"""
n=int(input())
ans = (n*(n+1))/2
print(int(ans))

"""
Sum Of Sqaures of First N natural Numbers:
"""
n=int(input())
ans = (n*(n+1)*(2*n+1))/6
print(int(ans))

"""
Sum Of Cubes of First N natural Numbers:
"""
n=int(input())
ans = (n*n*(n+1)*(n+1))/4
print(int(ans))